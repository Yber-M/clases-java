    private void jbtnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnMostrarActionPerformed

        String a = "", e = "";
        double b = 0, c = 0, ss = 0, cont = 0, d = 0, f = 0, mayor = -1, menor = 999999;

        for (int i = 0; i < NumEmp; i++) {

            if (ArregloEmp[i].sueldoNeto() > mayor) {
                mayor = ArregloEmp[i].sueldoNeto();
                a = ArregloEmp[i].nombre;
            }

            if (ArregloEmp[i].area.equals("Marketing")) {
                ss = ss + ArregloEmp[i].sueldoNeto();
                cont++;
                b = ss / cont;
            }

            if (ArregloEmp[i].montoSeguro() > 100) {
                c++;
            }

            if (ArregloEmp[i].area.equals("Sistemas") && ArregloEmp[i].montoHExtras() >= 500
                    && ArregloEmp[i].montoHExtras() <= 800) {
                d++;
            }

            if (ArregloEmp[i].tipoSeg.equals("SNP") && ArregloEmp[i].montoDescuento() < menor) {
                menor = ArregloEmp[i].montoDescuento();
                e = ArregloEmp[i].nombre;
            }

            f = f + ArregloEmp[i].sueldoBruto();

            //datosdesalida
            jtxta.setText(a);
            jtxtb.setText(String.valueOf(b));
            jtxtc.setText(String.valueOf(c));
            jtxtd.setText(String.valueOf(d));
            jtxte.setText(e);
            jtxtf.setText(String.valueOf(f));
        }
    }//GEN-LAST:event_jbtnMostrarActionPerformed

