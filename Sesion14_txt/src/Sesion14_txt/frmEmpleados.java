    void ExtraerDeArchivo() {
        String linea;
        try {
            FileReader fr = new FileReader("empleados.txt");
            BufferedReader br = new BufferedReader(fr);
            while ((linea = br.readLine()) != null) {
                StringTokenizer st = new StringTokenizer(linea, "|");
                String cod = st.nextToken();
                String nom = st.nextToken();
                String area = st.nextToken();
                double sueldo = Double.parseDouble(st.nextToken());
                int h_extras = Integer.parseInt(st.nextToken());
                String seguro = st.nextToken();

                UnEmp = new Empleados(cod, nom, area, sueldo, h_extras, seguro);
                ArregloEmp[NumEmp] = UnEmp;
                InsertarEnTabla(UnEmp);
                NumEmp++;
            }
            br.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error en la lectura"
                    + " del archivo ..." + ex);
        }
    }

    void GrabarEnArchivo(Empleados e) {

        try {
            FileWriter fw = new FileWriter("empleados.txt", true);
            PrintWriter pw = new PrintWriter(fw);
            pw.println(e.codigo + "|" + e.nombre + "|" + e.area + "|" + e.sueldobase + "|" + e.horasExtras + "|" + e.tipoSeg);
            pw.close();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error en la grabacion....." + ex);
        }

    }

    void InsertarEnTabla(Empleados UnEmp) {

        DecimalFormat df2 = new DecimalFormat("####.00");

        String me = df2.format(UnEmp.montoHExtras());
        String ms = df2.format(UnEmp.montoSeguro());
        String mes = df2.format(UnEmp.montoEssalud());
        String SN = df2.format(UnEmp.sueldoNeto());

        Object[] fila = {NumEmp + 1, UnEmp.codigo, UnEmp.nombre, UnEmp.area, UnEmp.tipoSeg,
            UnEmp.horasExtras, UnEmp.sueldobase, me, ms, mes, SN};

        ModeloTabla.addRow(fila);
    }

