    private void jbtnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCalcularActionPerformed
        // Marlon Yber Quispe Olano
        // Yb.M;
        
        String nom = jtxtNombre.getText(); 
        String code = jtxtCodeAlum.getText();
        double not1 = Double.parseDouble(jtxtNota1.getText());
        double not2 = Double.parseDouble(jtxtNota2.getText());
        double not3 = Double.parseDouble(jtxtNota3.getText());
        String course = jcbxCursos.getSelectedItem().toString();
        
        
        alum = new Alumno (nom, code, not1, not2, not3, course);
        jtxaMostrar.append("Código      : " + alum.cod + "\n");
        jtxaMostrar.append("Nombre      : " + alum.nombre + "\n");
        jtxaMostrar.append("Asignatura  : " + alum.asignatura + "\n");
        jtxaMostrar.append("Nota 1      : " + alum.getNot1() + "\n");
        jtxaMostrar.append("Nota 2      : " + alum.getNot2() + "\n");
        jtxaMostrar.append("Nota 3      : " + alum.getNot3() + "\n");
        jtxaMostrar.append("Promedio    : " + alum.promedio() +  "\n");
    }//GEN-LAST:event_jbtnCalcularActionPerformed

