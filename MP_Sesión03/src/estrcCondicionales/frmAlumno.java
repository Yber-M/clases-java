    String Verif () {
        if (jtxtNombre.getText().equals("")) return "Apellidos y Nombres";
        if (jtxtCodeAlum.getText().equals("")) return "código";
        if (jtxtNota1.getText().equals("") || Double.parseDouble(jtxtNota1.getText())< 0 || 
                Double.parseDouble(jtxtNota1.getText()) > 20) return "Nota 1";
        if (jtxtNota2.getText().equals("") || Double.parseDouble(jtxtNota2.getText())< 0 || 
                Double.parseDouble(jtxtNota2.getText()) > 20) return "Nota 2";
        if (jtxtNota3.getText().equals("") || Double.parseDouble(jtxtNota3.getText())< 0 || 
                Double.parseDouble(jtxtNota3.getText()) > 20) return "Nota 3";
        return Verif();
    }

